    /* ── CountUp ── */
    document.querySelectorAll('.bk-count-up').forEach(function (el) {
        var to = parseInt(el.dataset.to, 10);
        var started = false;
        var obs = new IntersectionObserver(function (entries) {
            if (!entries[0].isIntersecting || started) return;
            started = true;
            obs.disconnect();
            if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
                el.textContent = to.toLocaleString('en-US'); return;
            }
            var duration = 1200;
            var startTs = null;
            function ease(t) { return 1 - Math.pow(1 - t, 3); }
            function tick(ts) {
                if (!startTs) startTs = ts;
                var t = Math.min(1, (ts - startTs) / duration);
                el.textContent = Math.round(to * ease(t)).toLocaleString('en-US');
                if (t < 1) requestAnimationFrame(tick);
            }
            requestAnimationFrame(tick);
        }, { threshold: 0.5 });
        obs.observe(el);
    });

    /* ── Diagram + card hover sync ── */
    var nodes = Array.from(document.querySelectorAll('.bk-diagram-node'));
    var cards = Array.from(document.querySelectorAll('.bk-service-card'));
    var lines = [
