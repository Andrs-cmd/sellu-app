    ];

    function setActive(idx) {
        nodes.forEach(function (n, i) {
            n.classList.toggle('bk-node-active', i === idx);
        });
        cards.forEach(function (c, i) {
            c.classList.toggle('bk-card-active', i === idx);
        });
    }

    nodes.forEach(function (n) {
        n.addEventListener('mouseenter', function () { setActive(parseInt(n.dataset.idx, 10)); });
        n.addEventListener('click',      function () { setActive(parseInt(n.dataset.idx, 10)); });
    });
    cards.forEach(function (c) {
        c.addEventListener('mouseenter', function () { setActive(parseInt(c.dataset.idx, 10)); });
    });

    /* ── Diagram scroll reveal ── */
    var grid = document.getElementById('bk-diagram-grid');
    if (grid) {
        var revealed = false;
        var revObs = new IntersectionObserver(function (entries) {
            if (!entries[0].isIntersecting || revealed) return;
            revealed = true;
            revObs.disconnect();
            var reduce = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
            nodes.forEach(function (n, i) {
                var delay = reduce ? 0 : 250 + i * 380;
                setTimeout(function () {
                    n.classList.add('bk-node-visible');
                    // animate line after this node appears (except last)
                    if (i < lines.length && lines[i] && !reduce) {
                        setTimeout(function () { lines[i].style.strokeDashoffset = '0'; }, 200);
                    } else if (i < lines.length && lines[i] && reduce) {
                        lines[i].style.strokeDashoffset = '0';
                    }
                }, delay);
            });
