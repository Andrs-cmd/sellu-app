<script>
(function () {
    /* ── helper ── */
    function onVisible(el, cb, opts) {
        if (!el) return;
        const obs = new IntersectionObserver((entries) => {
            entries.forEach(e => { if (e.isIntersecting) { cb(); obs.disconnect(); } });
        }, opts || { threshold: 0.15 });
        obs.observe(el);
    }

    /* ── Hero entrance ── */
    document.querySelectorAll('#hero .anim').forEach((el, i) => {
        setTimeout(() => el.classList.add('in'), 80 + i * 80);
    });

    /* ── Risk ticker ── */
    const risks = [
        'producto retenido en aduana',
        'marca bloqueada en Amazon',
        'nombre registrado por un tercero',
        'cargamento devuelto al origen',
        'multas que superan los $25,000',
    ];
    const wrap = document.getElementById('riskWrap');
    if (wrap) {
        risks.forEach((r, i) => {
            const d = document.createElement('div');
            d.className = 'risk-text';
            d.textContent = r;
            d.style.transform = i === 0 ? 'translateY(0)' : 'translateY(100%)';
            d.style.opacity   = i === 0 ? '1' : '0';
            wrap.appendChild(d);
        });
        let idx = 0;
        setInterval(() => {
            const texts = wrap.querySelectorAll('.risk-text');
            texts[idx].style.transform = 'translateY(-100%)';
            texts[idx].style.opacity   = '0';
            idx = (idx + 1) % risks.length;
            texts[idx].style.transform = 'translateY(0)';
            texts[idx].style.opacity   = '1';
        }, 2400);
    }

    /* ── Flow diagram ── */
    onVisible(document.getElementById('heroFlow'), () => {
        [0, 1, 2].forEach(i => {
            setTimeout(() => {
                const fn = document.getElementById('fn' + i);
                if (fn) fn.classList.add('in');
            }, i * 500);
        });
        [0, 1].forEach(i => {
            const fc = document.getElementById('fc' + i);
            if (!fc) return;
            setTimeout(() => {
                fc.style.transition = 'stroke-dashoffset 500ms cubic-bezier(0.16,1,0.3,1)';
                fc.style.strokeDashoffset = '0';
            }, 300 + i * 600);
        });
    }, { threshold: 0.3 });

    /* ── Registry cards ── */
    onVisible(document.getElementById('regCards'), () => {
        ['rc0', 'rc1'].forEach(id => {
            const el = document.getElementById(id);
            if (el) el.classList.add('in');
        });
    });

    /* ── Comparison table ── */
    onVisible(document.getElementById('cmpTable'), () => {
        document.getElementById('cmpTable').classList.add('in');
    }, { threshold: 0.2 });

    /* ── Operar cards ── */
    onVisible(document.getElementById('operarCards'), () => {
        ['oc0', 'oc1', 'oc2'].forEach(id => {
            const el = document.getElementById(id);
            if (el) el.classList.add('in');
        });
    }, { threshold: 0.2 });

    /* ── Stepper ── */
    onVisible(document.getElementById('stepperWrap'), () => {
        const fill  = document.getElementById('stepLineFill');
        const start = performance.now();
        const dur   = 1600;
        (function tick(t) {
            const p = Math.min(1, (t - start) / dur);
            if (fill) fill.style.width = (p * 100) + '%';
            for (let i = 0; i < 5; i++) {
                const sp = Math.max(0, Math.min(1, p * 5 - i * 0.7));
                const sn = document.getElementById('sn' + i);
                const sc = document.getElementById('sc' + i);
                if (sn) sn.classList.toggle('popped', sp > 0.3);
                if (sc) sc.classList.toggle('in',     sp > 0.55);
            }
            if (p < 1) requestAnimationFrame(tick);
        })(performance.now());
    }, { threshold: 0.2 });

    /* ── CTA box ── */
    onVisible(document.getElementById('ctaBox'), () => {
        document.getElementById('ctaBox').classList.add('in');
    }, { threshold: 0.2 });

})();
</script>

