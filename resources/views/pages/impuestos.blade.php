function renderStep() {
    if (currentStep >= calcSteps.length) return renderResult();
    const s = calcSteps[currentStep];
    stepNum.textContent = `Paso ${currentStep + 1} de 4`;
    backBtn.disabled = currentStep === 0;
    segs.forEach((seg, i) => { seg.classList.remove('active', 'done'); if (i < currentStep) seg.classList.add('done'); else if (i === currentStep) seg.classList.add('active'); });
    calcBody.innerHTML = `<h3>${s.q}</h3><div class="calc-options ${s.layout}">${s.options.map(o => `<button class="calc-opt" data-val="${o.value}"><span>${o.label}</span><span class="arrow">→</span></button>`).join('')}</div>`;
    calcBody.querySelectorAll('.calc-opt').forEach(btn => {
        btn.addEventListener('click', () => {
            calcBody.querySelectorAll('.calc-opt').forEach(b => b.classList.remove('selected'));
            btn.classList.add('selected');
            calcAnswers[currentStep] = btn.dataset.val;
            setTimeout(() => { currentStep++; renderStep(); }, 280);
        });
    });
}

