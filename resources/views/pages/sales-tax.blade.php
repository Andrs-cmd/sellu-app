document.addEventListener("DOMContentLoaded", function () {
    const select = document.getElementById("nexus-select");
    const card   = document.getElementById("nexus-card");
    const data   = JSON.parse(document.getElementById("nexus-data").textContent);

    data.forEach(item => {
        const opt = document.createElement("option");
        opt.value = item.state;
        opt.textContent = item.state;
        select.appendChild(opt);
    });

    function showStateInfo(stateName) {
        const s = data.find(d => d.state === stateName);
        if (!s) return;
        document.getElementById("nxs-effective").textContent  = s.effective_date;
        document.getElementById("nxs-threshold").textContent  = s.threshold;
        document.getElementById("nxs-measure").textContent    = s.measurement_date;
        document.getElementById("nxs-includable").textContent = s.includable_sales;
        document.getElementById("nxs-when").textContent       = s.when_register;
        document.getElementById("nxs-rate").textContent       = s.state_rate;
        const link = document.getElementById("nxs-link");
        link.textContent = s.more_info_text;
        link.href        = s.more_info_url;
        card.style.display = "block";
    }

    select.addEventListener("change", e => showStateInfo(e.target.value));
    showStateInfo("Florida");
    select.value = "Florida";
});
