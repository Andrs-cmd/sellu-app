    {{-- 1. Garantía de Servicios --}}
    <div class="t-section">
        <h2>1. Garantía de Servicios</h2>

        <h3>1.1 Alcance de la Garantía</h3>
        <p>SELL-U LATAM LLC garantiza que los servicios prestados:</p>
        <ul>
            <li>Serán ejecutados con profesionalismo y conforme a los estándares comerciales de la industria.</li>
            <li>Cumplirán con las especificaciones acordadas en los contratos individuales con el Cliente.</li>
            <li>Serán entregados dentro de los plazos establecidos, salvo eventos de fuerza mayor.</li>
        </ul>
        <p>Esta garantía cubre errores atribuibles a la Compañía en la ejecución del servicio, incluyendo defectos en la entrega de consultorías, errores administrativos o incumplimientos de términos previamente acordados.</p>

        <h3>1.2 Exclusiones de Garantía</h3>
        <p>La garantía no cubre:</p>
        <ul>
            <li>Resultados específicos en la expansión comercial o ventas del Cliente.</li>
            <li>Errores derivados de información incorrecta proporcionada por el Cliente.</li>
            <li>Daños o pérdidas indirectas resultantes del uso de nuestros servicios.</li>
            <li>Modificaciones o alteraciones de los servicios por parte del Cliente o terceros sin autorización.</li>
        </ul>
    </div>

    {{-- 2. Política de Reembolsos y Cancelaciones --}}
    <div class="t-section">
        <h2>2. Política de Reembolsos y Cancelaciones</h2>

        <h3>2.1 Reembolsos</h3>
        <p>Los reembolsos solo se emitirán bajo las siguientes condiciones:</p>
        <ul>
            <li>Si SELL-U LATAM LLC no cumple con los términos acordados en la prestación del servicio.</li>
            <li>Si un servicio no ha sido entregado dentro del plazo establecido y no se ha logrado una solución alternativa.</li>
            <li>Si el Cliente ha realizado un pago duplicado o erróneo.</li>
        </ul>

        <h3>Proceso de Solicitud de Reembolso</h3>
        <p>Para solicitar un reembolso, el Cliente debe:</p>
        <ol>
            <li>Enviar una solicitud por escrito a <a href="mailto:contacto@sellu.co" style="color:var(--gold);font-weight:600;">contacto@sellu.co</a> con la siguiente información:
                <ul style="margin-top:6px;">
                    <li>Nombre completo.</li>
                    <li>Número de contrato o factura.</li>
                    <li>Motivo de la solicitud de reembolso.</li>
                    <li>Pruebas o documentación relevante.</li>
                </ul>
            </li>
            <li>La Compañía evaluará la solicitud en un plazo de <strong>15 días hábiles</strong>.</li>
            <li>Si la solicitud es aprobada, el reembolso se procesará a través del mismo método de pago utilizado por el Cliente en un plazo de <strong>10 días hábiles</strong>.</li>
        </ol>
        <p>No se emitirán reembolsos por servicios ya completados y entregados según lo acordado.</p>

        <h3>2.2 Cancelaciones</h3>

        <h3>Cancelación por parte del Cliente</h3>
        <ul>
            <li>El Cliente podrá cancelar un servicio notificando por escrito con al menos <strong>5 días hábiles</strong> de anticipación.</li>
            <li>Se podrá aplicar un reembolso parcial dependiendo del nivel de avance del servicio contratado.</li>
            <li>En caso de que el servicio haya sido iniciado y se desee cancelar, SELL-U LATAM LLC podrá retener una parte del pago correspondiente a los costos administrativos y al trabajo ya realizado.</li>
        </ul>

        <h3>Cancelación por parte de SELL-U LATAM LLC</h3>
        <p>SELL-U LATAM LLC se reserva el derecho de cancelar un servicio si:</p>
        <ul>
            <li>El Cliente incumple con sus obligaciones contractuales.</li>
            <li>Se presentan situaciones fuera del control razonable de la Compañía.</li>
        </ul>
        <p>En estos casos, se evaluará un posible reembolso proporcional según lo establecido en la sección 2.1.</p>
    </div>

    {{-- 3. Limitación de Responsabilidad --}}
    <div class="t-section">
        <h2>3. Limitación de Responsabilidad</h2>
        <p>En la medida permitida por la ley, SELL-U LATAM LLC no será responsable por:</p>
        <ul>
            <li>Pérdidas indirectas, incidentales o consecuentes derivadas del uso de sus servicios.</li>
            <li>Resultados esperados no alcanzados por el Cliente.</li>
            <li>Problemas derivados de información incorrecta proporcionada por el Cliente.</li>
        </ul>
        <p>Nuestra responsabilidad total se limita al monto pagado por el Cliente por los servicios contratados.</p>
    </div>

