// ── Category pick → pre-select form ──────────────────────────
function pickCategory(service) {
    // Pre-select radio
    const radio = document.querySelector(`input[name="servicio"][value="${service}"]`);
    if (radio) {
        radio.checked = true;
        updateServiceUI();
    }
    // Show preset notice
    const notice = document.getElementById('preset-notice');
    notice.style.display = 'flex';
    // Scroll to form
    document.getElementById('formulario').scrollIntoView({ behavior: 'smooth' });
}

// ── Form service UI ───────────────────────────────────────────
const serviceIds = ['llc','itin','amazon','fda','trademark','general'];
function updateServiceUI() {
    const val = document.querySelector('input[name="servicio"]:checked')?.value;
    serviceIds.forEach(id => {
        const el = document.getElementById('srv-' + id);
        if (el) el.classList.toggle('active', val === id);
    });
}

// ── Canal radio UI ────────────────────────────────────────────
function setCanal(val) {
    ['email','whatsapp','call'].forEach(c => {
        const el = document.getElementById('canal-' + c);
        const radio = el?.querySelector('input');
        if (el) el.classList.toggle('active', c === val);
        if (radio) radio.checked = (c === val);
    });
}

